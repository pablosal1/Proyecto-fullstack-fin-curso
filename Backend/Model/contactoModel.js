const mongoose = require('mongoose');

const contactoSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const Contacto = mongoose.model('Contacto', contactoSchema);

module.exports = Contacto;
